
# Install R packages required for the "Introduction to Econometrics Using R" course.

install.packages("tidyverse")   # data manipulation and visualization (includes dplyr, ggplot2, readxl, broom)
install.packages("questionr")   # frequency tables
install.packages("gmodels")     # CrossTable()
install.packages("GGally")      # ggpairs() scatter-plot matrix
install.packages("car")         # regression diagnostics (vif, ncvTest, outlierTest, …)
install.packages("wooldridge")  # Wooldridge textbook datasets
install.packages("AER")         # ivreg(), coeftest(), vcovHC()
install.packages("plm")         # panel data models
install.packages("stargazer")   # formatted regression tables
install.packages("margins")     # average partial effects for logit/probit
install.packages("pscl")        # McFadden pseudo-R² (pR2())
